    <h1 class="text-3xl font-semibold">Protokol Jaringan</h1>
    <h2 class="text-2xl font-semibold">C. Pendalaman Protokol Jaringan</h2>

    <p>Di bawah ini adalah penjelasan lebih rinci tentang protokol jaringan:</p>

    <ol class="list-decimal pl-4">
        <li>
            <strong>Tujuan Protokol Jaringan:</strong>
            <ul class="list-disc list-inside pl-4">
                <li>Protokol jaringan digunakan untuk memastikan komunikasi yang efisien, andal, dan aman antara perangkat dalam jaringan.</li>
                <li>Protokol ini menentukan cara data dipecah menjadi paket-paket, bagaimana paket-paket tersebut dikirimkan, dan cara mengatasi masalah yang mungkin terjadi selama komunikasi.</li>
            </ul>
        </li>

        <li>
            <strong>Lapisan Protokol:</strong>
            <p>Protokol jaringan sering dibagi menjadi lapisan-lapisan, yang masing-masing bertanggung jawab untuk tugas-tugas tertentu. Contoh lapisan umum meliputi:</p>
            <ul class="list-disc list-inside pl-4">
                <li><strong>Lapisan Fisik:</strong> Mengatur perangkat keras fisik yang digunakan untuk mentransmisikan data, seperti kabel atau gelombang radio.</li>
                <li><strong>Lapisan Data Link:</strong> Mengatur akses ke media fisik dan mengatasi masalah dalam pengiriman data pada lapisan fisik.</li>
                <li><strong>Lapisan Jaringan:</strong> Menentukan bagaimana data diarahkan melalui jaringan, termasuk pengalamatan (seperti alamat IP) dan routing.</li>
                <li><strong>Lapisan Transport:</strong> Bertanggung jawab atas pengiriman data end-to-end, termasuk pengontrolan aliran (flow control) dan pemulihan kesalahan (error recovery).</li>
                <li><strong>Lapisan Aplikasi:</strong> Menyediakan antarmuka bagi aplikasi untuk berkomunikasi melalui jaringan, termasuk protokol seperti HTTP, FTP, dan SMTP.</li>
            </ul>
        </li>

        <li>
            <strong>Protokol Ternama:</strong>
            <p>Ada banyak protokol jaringan yang telah dikembangkan untuk berbagai keperluan. Beberapa protokol jaringan terkenal antara lain:</p>
            <ul class="list-disc list-inside pl-4">
                <li><strong>TCP/IP:</strong> Protokol inti Internet yang digunakan untuk menghubungkan perangkat dan mengirim data di seluruh dunia.</li>
                <li><strong>HTTP/HTTPS:</strong> Protokol yang digunakan untuk mengakses situs web (HTTP) dan untuk mengakses situs web yang aman (HTTPS).</li>
                <li><strong>SMTP/POP3/IMAP:</strong> Protokol untuk mengirim dan menerima email.</li>
                <li><strong>FTP:</strong> Protokol untuk mentransfer file antara perangkat.</li>
                <li><strong>DNS:</strong> Protokol yang mengaitkan nama domain dengan alamat IP.</li>
            </ul>
        </li>

        <li>
            <strong>Keamanan Protokol:</strong>
            <ul class="list-disc list-inside pl-4">
                <li>Keamanan sangat penting dalam protokol jaringan. Protokol seperti HTTPS menggunakan enkripsi untuk melindungi data yang dikirimkan melalui jaringan.</li>
                <li>Protokol keamanan seperti SSL/TLS digunakan untuk melindungi data sensitif saat transit.</li>
            </ul>
        </li>

        <li>
            <strong>Standar Protokol:</strong>
            <ul class="list-disc list-inside pl-4">
                <li>Banyak protokol jaringan adalah standar yang telah ditetapkan oleh organisasi seperti Internet Engineering Task Force (IETF) atau Institute of Electrical and Electronics Engineers (IEEE).</li>
                <li>Standar ini memastikan bahwa perangkat dari berbagai vendor dapat berkomunikasi dengan benar.</li>
            </ul>
        </li>

        <li>
            <strong>Implementasi Protokol:</strong>
            <p>Protokol jaringan diimplementasikan dalam perangkat keras (misalnya, router dan switch) dan perangkat lunak (misalnya, sistem operasi dan aplikasi).</p>
        </li>

        <li>
            <strong>Contoh Kasus Penggunaan:</strong>
            <ul class="list-disc list-inside pl-4">
                <li>Protokol jaringan digunakan dalam berbagai lingkungan, termasuk jaringan lokal (LAN), jaringan luas (WAN), dan Internet.</li>
                <li>Mereka juga digunakan dalam komunikasi antar perangkat seluler, Internet of Things (IoT), dan banyak lagi.</li>
            </ul>
        </li>

        <li>
            <strong>Evolusi Protokol:</strong>
            <p>Protokol jaringan terus berkembang untuk memenuhi tuntutan komunikasi yang semakin kompleks dan kebutuhan keamanan yang meningkat.</p>
        </li>
    </ol>

    <p>Dengan protokol jaringan yang tepat, perangkat dapat berkomunikasi dengan efisien, data dapat dikirimkan secara aman, dan jaringan dapat berfungsi dengan baik. Ini membuat protokol jaringan menjadi pondasi penting dalam dunia komputasi modern.</p>
</div>