    <h2 class="text-2xl font-semibold">C. Penggunaan IP Address</h2>

    <p>
        IP Address memiliki berbagai penggunaan penting dalam jaringan komputer yang mencakup:
    </p>

    <h3 class="text-xl font-semibold">1. Rute dan Pengiriman Paket Data</h3>
    <p>
        IP Address digunakan sebagai alamat yang unik untuk perangkat dalam jaringan. Ketika data dikirim melalui jaringan, router dan perangkat jaringan lainnya menggunakan IP Address untuk merutekan paket data ke perangkat yang tepat. Dengan demikian, IP Address memainkan peran kunci dalam pengiriman data dalam jaringan. Data dikirimkan dari perangkat sumber ke perangkat tujuan dengan memanfaatkan informasi dalam header paket yang mencakup alamat IP pengirim dan penerima.
    </p>

    <h3 class="text-xl font-semibold">2. Identifikasi Lokasi Geografis</h3>
    <p>
        Beberapa alamat IP, terutama yang digunakan oleh penyedia layanan internet (ISP), dapat digunakan untuk mengidentifikasi lokasi geografis kasar dari perangkat yang terhubung. Informasi ini digunakan dalam berbagai konteks, termasuk dalam penyediaan layanan berbasis lokasi dan analisis data geografis. Namun, penting untuk dicatat bahwa identifikasi lokasi berdasarkan IP Address seringkali tidak begitu presisi dan dapat hanya memberikan informasi kasar tentang wilayah geografis perangkat tersebut.
    </p>

    <h3 class="text-xl font-semibold">3. Keamanan Jaringan</h3>
    <p>
        IP Address juga memiliki peran penting dalam aspek keamanan jaringan. Administrator jaringan dapat menggunakan IP Address dalam pengaturan keamanan, seperti firewall, untuk mengontrol akses ke jaringan. Dengan memblokir atau membatasi akses berdasarkan IP Address, administrator dapat memitigasi risiko peretasan dan serangan dari alamat IP yang tidak diinginkan. Selain itu, aktivitas jaringan dapat dilacak dan diawasi dengan menggunakan log IP Address, memungkinkan deteksi dan respons terhadap aktivitas yang mencurigakan atau berpotensi berbahaya dalam jaringan.
    </p>

    <p>
        Penggunaan IP Address adalah komponen kunci dalam fungsi dan keamanan jaringan modern. Pemahaman yang baik tentang bagaimana IP Address digunakan dalam berbagai aspek komunikasi dan keamanan jaringan penting untuk pengelolaan yang efisien dan aman dari jaringan komputer.
    </p>
