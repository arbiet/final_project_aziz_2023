    <h1 class="text-3xl font-semibold">IP Address</h1>
    <h2 class="text-2xl font-semibold">B. Jenis-jenis IP Address</h2>

    <p>
        Terdapat berbagai jenis IP Address yang digunakan dalam jaringan komputer, masing-masing dengan karakteristik dan tujuan penggunaan yang berbeda. Berikut adalah penjelasan lebih detail tentang jenis-jenis IP Address berdasarkan tipe:
    </p>

    <h3 class="text-xl font-semibold">1. IPv4 (Internet Protocol version 4)</h3>
    <p>
        IPv4 adalah format IP Address yang paling umum digunakan saat ini. Formatnya menggunakan 32-bit dan terdiri dari empat oktet yang dipisahkan oleh titik. Contoh IPv4: 192.168.0.1. Ini adalah format yang paling dikenal dan telah digunakan selama beberapa dekade.
    </p>

    <h3 class="text-xl font-semibold">2. IPv6 (Internet Protocol version 6)</h3>
    <p>
        IPv6 adalah format IP Address yang dikembangkan sebagai langkah maju dari IPv4 untuk mengatasi masalah kehabisan alamat. IPv6 menggunakan 128-bit dan ditulis dalam format heksadesimal. Contoh IPv6: 2001:0db8:85a3:0000:0000:8a2e:0370:7334. Dengan jumlah alamat yang sangat besar, IPv6 memungkinkan perkembangan internet yang lebih luas dan mendukung perangkat-perangkat baru yang terhubung ke jaringan.
    </p>

    <h3 class="text-xl font-semibold">3. IP Public vs. IP Private</h3>
    <p>
        <strong>IP Public:</strong> Alamat IP Public digunakan untuk mengidentifikasi perangkat di internet secara global. Setiap perangkat yang terhubung ke internet membutuhkan IP Public yang unik agar dapat diakses secara global. IP Public digunakan untuk mengidentifikasi perangkat di luar jaringan lokal (LAN) dan dapat diakses dari internet.
    </p>
    <p>
        <strong>IP Private:</strong> Alamat IP Private digunakan dalam jaringan lokal (LAN) dan tidak dapat diakses langsung dari internet. IP Private digunakan secara internal dalam jaringan, dan perangkat-perangkat dalam LAN mungkin memiliki alamat IP Private yang sama. Biasanya, IP Private di-NAT (Network Address Translation) saat terhubung ke internet melalui IP Public.
    </p>

    <h3 class="text-xl font-semibold">4. Static vs. Dynamic IP Address</h3>
    <p>
        <strong>Static IP Address:</strong> Static IP Address adalah alamat yang tetap untuk sebuah perangkat dalam jaringan. Alamat ini tidak berubah seiring waktu dan biasanya digunakan untuk perangkat dengan fungsi khusus, seperti server. Static IP Address memastikan bahwa perangkat memiliki alamat yang konsisten, sehingga dapat diakses dengan mudah oleh pengguna atau perangkat lain.
    </p>
    <p>
        <strong>Dynamic IP Address:</strong> Dynamic IP Address adalah alamat yang dapat berubah secara periodik setiap kali perangkat terhubung ke jaringan. Dynamic IP Address umumnya digunakan untuk perangkat yang tidak memerlukan alamat tetap. Keuntungannya adalah pemberian alamat IP dinamis dapat menghemat alamat IP yang tersedia dalam jaringan dan dapat secara otomatis dikelola oleh server DHCP (Dynamic Host Configuration Protocol).
    </p>

    <p>
        Pemahaman yang baik tentang berbagai jenis IP Address ini sangat penting dalam pengaturan dan manajemen jaringan. Pemilihan jenis IP Address yang sesuai tergantung pada kebutuhan dan tujuan jaringan yang sedang dibangun atau dikelola.
    </p>
