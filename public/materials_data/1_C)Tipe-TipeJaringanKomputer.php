    <h1 class="text-3xl font-semibold">Pengenalan Jaringan</h1>
    <h2 class="text-2xl font-semibold">C. Tipe-tipe Jaringan Komputer</h2>

    <p>Terdapat beberapa tipe jaringan komputer yang berbeda, yang digunakan untuk berbagai keperluan dan lingkungan. Berikut adalah penjelasan tentang beberapa tipe jaringan komputer yang paling umum:</p>

    <ol class="list-decimal pl-4">
        <li>
            <h3>LAN (Local Area Network - Jaringan Area Lokal)</h3>
            <p><strong>Deskripsi:</strong> LAN adalah jaringan yang biasanya terbatas pada area geografis yang relatif kecil, seperti dalam satu gedung atau kampus.</p>
            <p><strong>Karakteristik Utama:</strong> Koneksi LAN sangat cepat dan terbatas pada lokasi fisik yang kecil. Umumnya, LAN menggunakan teknologi seperti Ethernet dan Wi-Fi.</p>
            <p><strong>Penggunaan Umum:</strong> Digunakan dalam bisnis, rumah, dan lingkungan pendidikan untuk berbagi sumber daya seperti printer dan berkas, serta mengakses internet.</p>
        </li>

        <li>
            <h3>WAN (Wide Area Network - Jaringan Area Luas)</h3>
            <p><strong>Deskripsi:</strong> WAN mencakup area geografis yang lebih besar daripada LAN, sering kali mencakup kota, negara, atau bahkan seluruh dunia.</p>
            <p><strong>Karakteristik Utama:</strong> WAN menggunakan teknologi seperti saluran telepon, serat optik, dan satelit untuk menghubungkan LAN yang terpisah secara geografis.</p>
            <p><strong>Penggunaan Umum:</strong> Digunakan oleh perusahaan besar, penyedia layanan internet, dan pemerintah untuk menghubungkan cabang-cabang, kantor pusat, dan pusat data.</p>
        </li>

        <li>
            <h3>MAN (Metropolitan Area Network - Jaringan Area Metropolitan)</h3>
            <p><strong>Deskripsi:</strong> MAN adalah jaringan yang mencakup area geografis yang lebih besar daripada LAN tetapi lebih kecil daripada WAN, seperti sebuah kota besar atau daerah metropolitan.</p>
            <p><strong>Karakteristik Utama:</strong> Biasanya menggunakan teknologi seperti serat optik atau kabel koaksial untuk menyediakan koneksi yang cepat di dalam area metropolitan.</p>
            <p><strong>Penggunaan Umum:</strong> Digunakan oleh penyedia layanan komunikasi dan lembaga pendidikan untuk menghubungkan kampus dan area perkotaan.</p>
        </li>

        <li>
            <h3>PAN (Personal Area Network - Jaringan Area Personal)</h3>
            <p><strong>Deskripsi:</strong> PAN adalah jaringan yang sangat kecil dan biasanya mencakup perangkat pribadi, seperti smartphone, tablet, dan laptop.</p>
            <p><strong>Karakteristik Utama:</strong> PAN biasanya menggunakan teknologi nirkabel seperti Bluetooth atau Infrared (IR) untuk menghubungkan perangkat secara lokal.</p>
            <p><strong>Penggunaan Umum:</strong> Digunakan untuk menghubungkan perangkat pribadi dan memfasilitasi pertukaran data antara perangkat yang berdekatan.</p>
        </li>

        <li>
            <h3>CAN (Campus Area Network - Jaringan Area Kampus)</h3>
            <p><strong>Deskripsi:</strong> CAN adalah jaringan yang mencakup area yang lebih besar daripada LAN tetapi biasanya terbatas pada wilayah kampus, seperti universitas atau kompleks industri.</p>
            <p><strong>Karakteristik Utama:</strong> Menggunakan teknologi LAN tetapi dapat menjangkau area yang lebih luas, termasuk gedung-gedung yang berjarak jauh di dalam kampus.</p>
            <p><strong>Penggunaan Umum:</strong> Digunakan di lingkungan pendidikan tinggi dan industri untuk menghubungkan berbagai gedung dan fasilitas dalam satu area kampus.</p>
        </li>

        <li>
            <h3>SAN (Storage Area Network - Jaringan Area Penyimpanan)</h3>
            <p><strong>Deskripsi:</strong> SAN adalah jenis jaringan yang dirancang khusus untuk penyimpanan data dan perangkat penyimpanan seperti server penyimpanan dan sistem RAID.</p>
            <p><strong>Karakteristik Utama:</strong> Terfokus pada penyediaan akses cepat ke penyimpanan data dan dapat menghubungkan banyak perangkat penyimpanan ke jaringan.</p>
            <p><strong>Penggunaan Umum:</strong> Digunakan oleh perusahaan dan pusat data untuk mengelola data yang membutuhkan kecepatan tinggi dan ketersediaan tinggi.</p>
        </li>
    </ol>
</div>