    <h1 class="text-3xl font-semibold">Pengenalan Jaringan</h1>
    <h2 class="text-2xl font-semibold">D. Topologi Jaringan</h2>

    <p>Topologi jaringan adalah susunan fisik atau logis dari komponen-komponen dalam sebuah jaringan komputer. Ini mencakup bagaimana perangkat-perangkat seperti komputer, printer, switch, router, dan lainnya terhubung satu sama lain dan bagaimana data dan informasi mengalir di antara mereka. Ada beberapa jenis topologi jaringan yang berbeda, masing-masing dengan karakteristiknya sendiri. Berikut penjelasan tentang beberapa topologi jaringan yang umum:</p>

    <ol class="list-decimal list-inside pl-4">
        <li>
            <h3>Topologi Bintang (Star Topology):</h3>
            <p><strong>Dalam topologi bintang, setiap perangkat terhubung langsung ke sebuah pusat atau switch.</strong></p>
            <p>Semua perangkat berkomunikasi melalui pusat ini.</p>
            <p>Ini adalah topologi yang mudah dikelola dan memiliki isolasi yang baik terhadap gangguan.</p>
            <p>Namun, jika pusat mati, maka seluruh jaringan dapat terganggu.</p>
        </li>

        <li>
            <h3>Topologi Bus (Bus Topology):</h3>
            <p><strong>Dalam topologi bus, semua perangkat terhubung ke kabel pusat tunggal.</strong></p>
            <p>Data dikirim ke seluruh jaringan dan hanya perangkat yang dituju yang akan memprosesnya.</p>
            <p>Ini adalah topologi yang sederhana tetapi rentan terhadap gangguan jika kabel pusat rusak.</p>
        </li>

        <li>
            <h3>Topologi Cincin (Ring Topology)</h3>
            <p><strong>Dalam topologi cincin, setiap perangkat terhubung ke dua perangkat lainnya, membentuk lingkaran.</strong></p>
            <p>Data mengalir dalam satu arah melalui setiap perangkat hingga mencapai tujuan.</p>
            <p>Jika salah satu perangkat mati atau kabel putus, seluruh jaringan bisa terganggu.</p>
        </li>

        <li>
            <h3>Topologi Mesh (Mesh Topology)</h3>
            <p><strong>Dalam topologi mesh, setiap perangkat terhubung langsung ke setiap perangkat lainnya dalam jaringan.</strong></p>
            <p>Ini memberikan tingkat redundansi dan kehandalan yang tinggi karena jika satu jalur rusak, masih ada jalur lain untuk komunikasi.</p>
            <p>Namun, topologi ini memerlukan banyak kabel dan konfigurasi yang rumit.</p>
        </li>

        <li>
            <h3>Topologi Pohon (Tree Topology)</h3>
            <p><strong>Topologi pohon adalah gabungan dari beberapa topologi bintang yang terhubung melalui satu kabel utama.</strong></p>
            <p>Ini memungkinkan untuk menggabungkan keuntungan topologi bintang dan pohon.</p>
            <p>Jaringan besar dengan banyak perangkat sering menggunakan topologi ini.</p>
        </li>

        <li>
            <h3>Topologi Hibrid (Hybrid Topology)</h3>
            <p><strong>Topologi hibrid adalah kombinasi dari dua atau lebih jenis topologi yang berbeda.</strong></p>
            <p>Ini digunakan untuk mencapai kebutuhan khusus dalam jaringan yang kompleks.</p>
        </li>
    </ol>

    <p>Setiap topologi jaringan memiliki kelebihan dan kekurangannya sendiri. Pemilihan topologi yang tepat tergantung pada kebutuhan dan tujuan jaringan, serta faktor-faktor seperti biaya, kehandalan, dan kemudahan pengelolaan. Seiring dengan itu, teknologi modern juga menggabungkan berbagai topologi dalam jaringan yang lebih kompleks dan canggih.</p>
