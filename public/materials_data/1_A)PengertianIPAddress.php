    <h1 class="text-3xl font-semibold">IP Address</h1>
    <h2 class="text-2xl font-semibold">A. Pengertian IP Address</h2>

    <p>
        IP Address (Internet Protocol Address) adalah alamat numerik unik yang diberikan kepada setiap perangkat yang terhubung ke jaringan TCP/IP (Transmission Control Protocol/Internet Protocol). Ini adalah salah satu komponen paling fundamental dalam arsitektur jaringan modern. Pengertian IP Address mencakup beberapa konsep penting:
    </p>

    <ul class="list-disc list-inside pl-4">
        <li>
            <strong>Identifikasi Perangkat:</strong>
            IP address digunakan untuk mengidentifikasi setiap perangkat dalam jaringan. Ini mirip dengan alamat fisik (alamat rumah) bagi perangkat dalam dunia jaringan.
        </li>
        <li>
            <strong>Pengalamatan:</strong>
            Dengan adanya IP address, paket data dapat diarahkan secara efisien ke perangkat yang tepat dalam jaringan. Ini memungkinkan komunikasi yang andal antara perangkat-perangkat tersebut.
        </li>
        <li>
            <strong>Unik:</strong>
            Setiap IP address harus unik dalam jaringan tertentu. Ini memastikan bahwa tidak ada konflik yang terjadi di antara perangkat dalam jaringan yang menggunakan alamat yang sama.
        </li>
        <li>
            <strong>Format Numerik:</strong>
            IP address biasanya berbentuk angka-angka, baik dalam format desimal (seperti 192.168.1.1) atau format biner. Kode biner ini merupakan representasi internal yang digunakan oleh perangkat untuk routing data.
        </li>
    </ul>

    <p>
        IP address adalah bagian integral dari komunikasi internet. Ketika Anda terhubung ke internet, perangkat Anda diberi IP address, baik itu dalam bentuk statis (tetap) atau dinamis (berubah setiap kali Anda terhubung). Dengan penggunaan IP address, jaringan dapat mengenali dan mengarahkan data ke tujuan yang benar, sehingga memungkinkan Anda untuk menjelajahi internet, mengirim email, dan melakukan berbagai aktivitas online lainnya.
    </p>
